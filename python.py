#String type (str)

x="Sashreeik"
print(type(x))

#Integer type (Int)

x=18
print(type(x))

#Float type

x=10.26
print(type(x))

#Complex type (Comp)

x=1j
print(type(x))

#List type

x=["hands","head"]
print(type(x))

#Tuple type

x=("hands","head")
print(type(x))

#Range type

x= range(10)
print(type(x))

#Dictionary type (Dict)

x={"Name":"Ram","Age":13}
print(type(x))

#Set type

x={"hands","head"}
print(type(x))

#Frozenset type

x=frozenset({"hands","head"})
print(type(x))

#Bytes type

x=b"Hello"
print(type(x))

#Bytearray type

x=bytearray(8)
print(type(x))

#Boolean type (Bool)

x=False
print(type(x))

#Memoryview type

x=memoryview(bytes(8))
print(type(x))

#None type

x=None
print(type(x))





